namespace XFormsTouch
{
    /// <summary>
    /// Possible touch action types.
    /// </summary>
    public enum TouchActionType
    {
        /// <summary>
        /// A touch entered.
        /// </summary>
        Entered,

        /// <summary>
        /// A touch pressed.
        /// </summary>
        Pressed,

        /// <summary>
        /// A touch moved.
        /// </summary>
        Moved,

        /// <summary>
        /// A touch was released.
        /// </summary>
        Released,

        /// <summary>
        /// A touch exited.
        /// </summary>
        Exited,

        /// <summary>
        /// A touch was cancelled.
        /// </summary>
        Cancelled,
    }
}
